package com.muhammethoca.Map;

public class Bolum {
	long id;
	String ad;

	public Bolum(long id, String ad) {
		super();
		this.id = id;
		this.ad = ad;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

}
